# 2D Lists = a list of lists

drinks = ["coke", "sprite", "coffee","tea"]
dinner = ["Poli bhaji", "varan bhat", "nan", "paneer"]
dessert = ["ice cream", "jalebi","golab jamun"]

all = [drinks,dinner,dessert]

print(all)              # Whole list
print(all[0])           # Specific list in list
print(all[1][0])        # Specific item in selective list


